public class Main {
    public static void main(String[] args) {

        int ageYears = 20;         
        int ageMonths;
        int ageDays;

        ageMonths = ageYears * 12;     
        ageDays = ageYears * 365;     

        System.out.println("Age in years: " + ageYears);
        System.out.println("Age in months: " + ageMonths);
        System.out.println("Age in days: " + ageDays);
    }
}
